console.log("Loop Concept");

//! While loop
// while ([condition]) {}
console.log("While LOOP");
let i = 0;
while (i < 10) {
  console.log(i);
  i++;
}

//! Do while()
//syntax
/* do {
  //lines of codes
} while ([condition]);
*/
//exmaple
console.log("Do while LOOP");
let ig = 0;
do {
  console.log(ig);
  ig++;
} while (ig < 10);

//! forLoop
//syntax
//for ([initilisation]; [condition]; [finalexpression]) {}
//example
console.log("For LOOP");
for (let i = 0; i < 10; i++) {
  console.log(i);
}
